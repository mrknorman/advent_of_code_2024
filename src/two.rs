fn check_saftey(
	report : Vec<i8>,
	max_difference : i8,
	min_difference : i8,
) -> usize {
	let mut last = report[0];
		let descending = report[0] > report[1];
		for mesurement in &report[1..] {
			let diff = mesurement - last;

			let big_gap = diff.abs() > max_difference || diff.abs() < min_difference;
			let not_decending = descending && &last < mesurement;
			let not_ascending = !descending && &last > mesurement;

			if big_gap || not_decending || not_ascending {
				return 1;
			}

			last = *mesurement;
		}

		return 0;
}

pub fn two_a(){

	let max_difference: i8 = 3;
	let min_difference : i8 = 1;

	let reports = reports();
	let mut num_safe_reports = reports.len();

	for report in reports{
		num_safe_reports -= check_saftey(
			report, 
			max_difference, 
			min_difference
		);
	}

	println!("The number of safe reports is {}", num_safe_reports);
}

pub fn two_b(){

	let max_difference: i8 = 3;
	let min_difference : i8 = 1;

	let reports = reports();
	let mut num_safe_reports = 0;

	for report in reports{

		for index in 0..report.len() {

			let mut report = report.clone();
			report.remove(index);
			if check_saftey(
				report, 
				max_difference, 
				min_difference,
			) == 0 {
				num_safe_reports += 1;
				break;
			}
		}
	}

	println!("The number of safe reports is {}", num_safe_reports);
}

