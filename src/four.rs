fn check_middle(
    i_init: i32,
    j_init: i32,
    grid: &[Vec<char>],
    word: &[char],
	directions: &Vec<(i32, i32)>
) -> i32 {

    let mut count = 0;
    'outer: for (i_inc, j_inc) in directions {
        let mut i = i_init + i_inc;
        let mut j = j_init + j_inc;

        for &char in word {
            if char != get_char(i, j, grid) {
                continue 'outer;
            }

            i -= i_inc;
            j -= j_inc;
        }

        count += 1;
    }

    count
}

