fn get_char(i : i32, j : i32, grid : &[Vec<char>]) -> char {
	if let (Ok(i_usize), Ok(j_usize)) = (usize::try_from(i), usize::try_from(j)) {
        grid.get(i_usize)
            .and_then(|row| row.get(j_usize))
            .copied()
            .unwrap_or('.')
    } else {
        '.'
    }
}


fn check_letter(
    i_init: i32,
    j_init: i32,
    grid: &[Vec<char>],
    word: &[char],
	directions: &Vec<(i32, i32)>
) -> i32 {

    let mut count = 0;
    'outer: for (i_inc, j_inc) in directions {
        let mut i = i_init;
        let mut j = j_init;

        for &char in word {
            if char != get_char(i, j, grid) {
                continue 'outer;
            }

            i += i_inc;
            j += j_inc;
        }

        count += 1;
    }

    count
}


